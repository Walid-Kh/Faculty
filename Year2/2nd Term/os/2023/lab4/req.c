#include <stdio.h>
#include <signal.h>
#include <stdlib.h>
#include <unistd.h>
#include <sys/wait.h>
#include <sys/types.h>

int CHILDREN_PID[2] = {0, 0};
int SEARCH_TERM;

void handler(int signum)
{
    int child;
    int stat_loc;
    child = wait(&stat_loc);
    int res = 0;
    for (int i = 0; i < 2; i++)
        if (child == CHILDREN_PID[i])
            res = i;
    printf("Child %d: Value %d found at position %d\n", res + 1, SEARCH_TERM, WEXITSTATUS(stat_loc));
    for (int i = 0; i < 2; i++)
    {
        kill(CHILDREN_PID[i], SIGKILL);
    }
    printf("KILLED\n");
    exit(0);
}

int main(int argc, char **argv)
{
    SEARCH_TERM = atoi(argv[1]);
    int *arr = (int *)malloc((argc - 2) * sizeof(int));
    int arrLen = argc - 2;
    {
        int index = 0;
        for (int i = 2; i < argc; i++)
            arr[index++] = atoi(argv[i]);
    }
    printf("I am the parent, PID = %d\n", getpid());
    for (int i = 0; i < 2; i++)
    {
        int pid = fork();
        if (pid == -1)
        {
            perror("error in fork");
        }
        else if (pid == 0)
        {
            // child
            if (i == 0)
                printf("I am the first child, PID = %d, PPID = %d\n", getpid(), getppid());
            else
                printf("I am the second child, PID = %d, PPID = %d\n", getpid(), getppid());
            for (int j = 0 + (i * ((arrLen / 2) + 1)); j < arrLen / (2 - i); j++)
            {
                if (arr[j] == SEARCH_TERM)
                {
                    kill(getppid(), SIGUSR1);
                    exit(j);
                }
            }
            sleep(3);
            printf("child #%d terminates\n", i + 1);
            exit(0);
        }
        else
        {
            signal(SIGUSR1, handler);
            // parent
            CHILDREN_PID[i] = pid;
        }
    }
    sleep(5);
    for (int i = 0; i < 2; i++)
        waitpid(CHILDREN_PID[i], NULL, 0);
    printf("Value not found\n");
}
